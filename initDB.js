const dataBase = require("better-sqlite3");

const db = dataBase("spaces.db");
db.prepare(
  `
    CREATE TABLE IF NOT EXISTS spaces (
    id INTEGER PRIMARY KEY AUTOINCREMENT, 
    slug TEXT NOT NULL UNIQUE, 
    title TEXT NOT NULL, 
    description TEXT NOT NULL, 
    image TEXT NOT NULL,
    gallery TEXT NOT NULL, 
    location TEXT NOT NULL, 
    tags TEXT NOT NULL, 
    price_per_day INTEGER NOT NULL, 
    price_per_mounth INTEGER NOT NULL
    )
    `
).run();

function initialDatabase() {
  const stmt = db.prepare(`
    INSERT INTO spaces (slug,title,description,image,gallery,location,tags,price_per_day,price_per_mounth) VALUES (
    @slug,
    @title,
    @description,
    @image,
    @gallery,
    @location,
    @tags,
    @price_per_day,
    @price_per_mounth
    )
    `);

  for (const space of dummmyPlaces) {
    space.tags = JSON.stringify(space.tags);
    space.gallery = JSON.stringify(space.gallery);
    stmt.run(space);
  }
}

initialDatabase();
